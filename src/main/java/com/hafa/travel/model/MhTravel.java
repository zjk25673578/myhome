package com.hafa.travel.model;

public class MhTravel {
    private Integer ids;

    private Integer homeid;

    private Integer userid;

    private String targets;

    private String detail;

    private Integer lastime;

    private Integer consume;

    private Byte isimpl;

    private Byte status;

    public Integer getIds() {
        return ids;
    }

    public void setIds(Integer ids) {
        this.ids = ids;
    }

    public Integer getHomeid() {
        return homeid;
    }

    public void setHomeid(Integer homeid) {
        this.homeid = homeid;
    }

    public Integer getUserid() {
        return userid;
    }

    public void setUserid(Integer userid) {
        this.userid = userid;
    }

    public String getTargets() {
        return targets;
    }

    public void setTargets(String targets) {
        this.targets = targets;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public Integer getLastime() {
        return lastime;
    }

    public void setLastime(Integer lastime) {
        this.lastime = lastime;
    }

    public Integer getConsume() {
        return consume;
    }

    public void setConsume(Integer consume) {
        this.consume = consume;
    }

    public Byte getIsimpl() {
        return isimpl;
    }

    public void setIsimpl(Byte isimpl) {
        this.isimpl = isimpl;
    }

    public Byte getStatus() {
        return status;
    }

    public void setStatus(Byte status) {
        this.status = status;
    }
}
